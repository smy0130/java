package study.class_.inheritancetest1;

public class Test1 {
	// 필드 영역
	private int a;
	private int b;
	// 생성자 영역
	public Test1() {}
	
	public Test1(int a, int b) {
		this.a = a;
		this.b = b;
	}
		
	// 메소드 영역
	
	// Getters/Setters 영역
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}	
	// 정적 영역
	
	// 동적 영역 
}
